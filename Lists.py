def map_to_rect(col_map):
    list_a = []
    list_b = []
    y = 0
    for row in col_map:
        x = 0
        for tile in row:
            if tile == '1':
                a = (x * 20, y * 20, 20, 20)
                list_a.append(a)
            if tile == '2':
                b = (x * 20, y * 20, 20, 20)
                list_b.append(b)
            x += 1
        y += 1
    return list_a, list_b


